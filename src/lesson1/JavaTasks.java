    //Асимптотика - O(N)
    //Ресурсоемкость - O(N)
    static public void sortSequence(String inputName, String outputName) {
        try (BufferedReader reader = new BufferedReader(new FileReader(inputName));
             FileWriter writer = new FileWriter(outputName)) {
            List<Integer> list = new ArrayList<>();
            Map<Integer, Integer> digits = new HashMap<Integer, Integer>();
            String line;
            while ((line = reader.readLine()) != null) {
                int i = Integer.valueOf(line);
                digits.put(i, digits.getOrDefault(i, 0) + 1);
                list.add(i);
            }
            int minDigit = Integer.MAX_VALUE;
            int countMaxMinDigit = 0;
            for (Integer key : digits.keySet()) {
                int currentDigitValue = digits.get(key);
                if (currentDigitValue > countMaxMinDigit) countMaxMinDigit = currentDigitValue;
            }
            for (Integer key : digits.keySet()) {
                int currentDigitValue = digits.get(key);
                if (currentDigitValue == countMaxMinDigit && key < minDigit) minDigit = key;
            }
            int j;
            for (j = 0; j < list.size(); j++) {
                int currentDigit = list.get(j);
                if (currentDigit != minDigit) writer.write(currentDigit + "\n");
            }
            for (j = 0; j < countMaxMinDigit; j++) writer.write(minDigit+ "\n");
        } catch (IOException e) {
            throw new NotImplementedError();
        }
    }

